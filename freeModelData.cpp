// IMPLEMENTATION FILE: "setAnalysis"
//====================================================================================================================================
// Syntax      : freeModelData(PATH, Coordinates, Elements, GaussPoints)
//------------------------------------------------------------------------------------------------------------------------------------
// Purpose     : Frees FEM variables of the analyzed Model. 
//------------------------------------------------------------------------------------------------------------------------------------
// Input       : Coordinates : List of coordinate values                          [Nnodes,3]
//               Elements    : List of elements values                            [Nelems,3]
//               GaussPoints : List of Gauss Integration values                   [Ngauss,3]
//------------------------------------------------------------------------------------------------------------------------------------
// Output      : Coordinates : Cleaned up list of coordinate values               [0,0]
//               Elements    : Cleaned up list of element values                  [0,0]
//               GaussPoints : Cleaned up list of Gauss Integration values        [0,0]
//------------------------------------------------------------------------------------------------------------------------------------
// Folder      : 
//------------------------------------------------------------------------------------------------------------------------------------
// Description : <Frees model memory>
// Keywords    : <Free,Coordinates, Elements, GaussPoints>
// Version     : <>
//====================================================================================================================================
// Written by Danilo S. Kusanovic, December 2013
// Last revised by D.S Kusanovic.
//====================================================================================================================================

  #include <iostream>
  
   void freeModelData(double** &Coordinates, int** &Elements, double** &GaussPoints, int** &Restraints, int** &Constraints, 
                      double* &Stiffness, double* &Force, int* &row, int* &col, int Nnodes, int Nelems, int Ngauss, int Nrestr, int Nconst){

//------------------------------------------------------------------------------------------------------------------------------------
// FREE COORDINATES DATA: 
//------------------------------------------------------------------------------------------------------------------------------------
     for(int i = 0; i < Nnodes; i++){
          delete[] Coordinates[i]; 
     }
     delete[] Coordinates;

//------------------------------------------------------------------------------------------------------------------------------------
// FREE ELEMENTS DATA: 
//------------------------------------------------------------------------------------------------------------------------------------
     for(int i = 0; i < Nelems; i++){
          delete[] Elements[i]; 
     }
     delete[] Elements;

//------------------------------------------------------------------------------------------------------------------------------------
// FREE GAUSS POINTS DATA: 
//------------------------------------------------------------------------------------------------------------------------------------
     for(int i = 0; i < Ngauss; i++){
          delete[] GaussPoints[i]; 
     }
     delete[] GaussPoints;

//------------------------------------------------------------------------------------------------------------------------------------
// FREE RESTRAINT DATA: 
//------------------------------------------------------------------------------------------------------------------------------------
     for(int i = 0; i < Nrestr; i++){
          delete[] Restraints[i]; 
     }
     delete[] Restraints;

//------------------------------------------------------------------------------------------------------------------------------------
// FREE RESTRAINT DATA: 
//------------------------------------------------------------------------------------------------------------------------------------
     for(int i = 0; i < Nconst; i++){
          delete[] Constraints[i]; 
     }
     delete[] Constraints;
//------------------------------------------------------------------------------------------------------------------------------------
// FREE STIFFNESS MATRIX DATA:
//------------------------------------------------------------------------------------------------------------------------------------
     delete[] row;
     delete[] col;
     delete[] Stiffness;

//------------------------------------------------------------------------------------------------------------------------------------
// FREE FORCE VECTOR DATA: 
//------------------------------------------------------------------------------------------------------------------------------------
     delete[] Force;

   }

